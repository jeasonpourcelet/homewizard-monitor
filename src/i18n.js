const LANGS = ['en', 'fr', 'nl'];

function pickLocale(raw) {
  const l = String(raw || '').slice(0, 2).toLowerCase();
  return LANGS.includes(l) ? l : 'en';
}

function makeT(locale) {
  const loc = LANGS.includes(locale) ? locale : 'en';
  return function t(key, vars) {
    let s = (STRINGS[loc] && STRINGS[loc][key]) ?? STRINGS.en[key] ?? key;
    if (vars) for (const k of Object.keys(vars)) s = s.split('{' + k + '}').join(String(vars[k]));
    return s;
  };
}

module.exports = { STRINGS, LANGS, pickLocale, makeT };
